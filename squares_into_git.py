# площадь правильного шестиугольника
def s6(a:int):
    '''площадь правильного шестиугольника. все стороны равна. - a длина стороны/радиус вписаной окружности'''
    return (3*(3**0.5)/2)*a**2

# площадь правильного пятиугольника
